#!/usr/bin/env python3
"""Module for the Dragon class."""


class Dragon:
    """Class representing a dragon."""

    def swim(self):
        """Make the dragon swim."""
        print("The creature swims!")

    def fly(self):
        """Make the dragon fly."""
        print("The creature flies!")

    def roar(self):
        """Make the dragon roar."""
        print("The dragon roars!")
